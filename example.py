# And statement
print("emma" == "emma" and "john" == "john")
print("marah" != "peter" and "peter" == "john")
print("Goat" == "Cow" and "Dog" == "Dog")
print("Pig" == "Bird" and "Snake" == "Amara") 

# OR statement
print("bush" == "bush" or "grass" == "grass")
print("rice" == "rice" or "beans" <= "rice")
print("bittered" <= "sweet" or "sweet" >= "sweet")
print("Bold" > "coward" or "marking" == "boarder")

# Not statement
print(not "pig" == "pig")
print(not "fan" != "fan")