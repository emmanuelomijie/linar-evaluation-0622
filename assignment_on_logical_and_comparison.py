# AND STATEMENT
print("ruth" == "ruth" and "john" == "john")
print("50" > "10" and "20" > "50")
print("dog" != "dog" and "funke" == "funke")
print("lion" > "lion" and "wolf" > "wolf")

# OR STATEMENT
print("50" == "50" or "60" == "60")
print("stew" == "stew" or "20" > "30")
print("book" <= "biro" or "biro" >= "biro")
print("television" != "television" or "chair" != "chair") 

# NOT STATEMENT
print(not "beans" == "beans")
print(not "20" != "20")
